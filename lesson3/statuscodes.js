// Common HTTP Status Codes
//---------------------------------------------------------------------------------------------------------------
// Status Code 200 – “OK” status code for a successful HTTP request.
//---------------------------------------------------------------------------------------------------------------
// Status Code 201 – Confirms that the request was successful(POST/PUT request)
//---------------------------------------------------------------------------------------------------------------
// Status Code 204 – Confirms that the server has fulfilled the request but does not need to return information.
//---------------------------------------------------------------------------------------------------------------
// Status Code 400 – The server cannot understand and process a request due to a client error. 
// Missing data, domain validation, and invalid formatting are some examples that cause the status code 400 to be sent.
//---------------------------------------------------------------------------------------------------------------
// Status Code 401 – Occurs when authentication is required but has failed or not been provided.
//---------------------------------------------------------------------------------------------------------------
// Status Code 403 – Happens when a valid request was sent, but the server refuses to accept it. This happens if a client/user requires the necessary permission or they may need an account to access the resource. Unlike a status code 401, authentication will not apply here.
//---------------------------------------------------------------------------------------------------------------
// Status Code 404 – Occurs when the request is valid, but the resource cannot be found on the server.
//---------------------------------------------------------------------------------------------------------------
// Status Code 410 – Resource requested is no longer available and will not be available again.
//---------------------------------------------------------------------------------------------------------------


